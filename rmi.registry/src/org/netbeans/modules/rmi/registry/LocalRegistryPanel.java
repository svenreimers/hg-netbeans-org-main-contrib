    private void setStatusMessage(LocalRegistry.Status status) {
        switch (status.getStatus()) {
            case LocalRegistry.RUNNING: 
                jTextField2.setText(NbBundle.getMessage(LocalRegistryPanel.class, "FMT_RegistryStarted", Integer.valueOf(status.getPort())));
                jButton1.setEnabled(false);
                jButton2.setEnabled(true);
                break;
            case LocalRegistry.STOPPED:
                jTextField2.setText(NbBundle.getMessage(LocalRegistryPanel.class, "FMT_RegistryStopped"));
                jButton1.setEnabled(true);
                jButton2.setEnabled(false);
                break;
            default:
                jTextField2.setText(NbBundle.getMessage(LocalRegistryPanel.class, "FMT_RegistryError"));
                jButton1.setEnabled(true);
                jButton2.setEnabled(true);
                break;
        }
    }

