    public HistoryOptionsPanel() {
        initComponents();
        
        Date now = new Date();
        jPanelFrom.add(from = new JDateChooser(now));
        jPanelTo.add(to = new JDateChooser(now));
        
        Dimension d = from.getPreferredSize();
        d.width += 10;
        from.setPreferredSize(d);
        to.setPreferredSize(d);
        
        durationPanel.setDuration(60);
    }
    
    /**
     * Returns the selected group.
     *
     * @return group.
     */
    public Group getGroup() {
        return Group.values()[jComboBoxGroup.getSelectedIndex()];
    }
    
    /**
     * Start date.
     *
     * @return start date
     */
    public Date getFrom() {
        return from.getDate();
    }
    
    /**
     * End date.
     *
     * @return end date
     */
    public Date getTo() {
        return to.getDate();
    }
            
    /**
     * Returns the minimum task duration in minutes.
     *
     * @return task duration in minutes.
     */
    public int getMinimumDuration() {
        return durationPanel.getDuration();
    }
    
    /**
     * Creates a ComboBoxModel for the group combobox.
     *
     * @return created model.
     */
