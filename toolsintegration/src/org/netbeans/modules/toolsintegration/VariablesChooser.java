    private void lVariablesValueChanged (javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lVariablesValueChanged
        Variable v = getSelectedVariable ();
        if (v == null) {
            taHelp.setText ("");
            return;
        }
        taHelp.setText (v.getVariableDescription ());
        tfSymbolicName.setText (v.getSymbolicName ());
        String symbolicName = v.getSymbolicName ();
        if (symbolicName.startsWith ("${argument")) {
            tfCurrentValue.setText ("");
            return;
        }
        String value = Variables.resolveVariables (symbolicName);
        if (value == null)
            tfCurrentValue.setText ("");
        else
            tfCurrentValue.setText (value);
    }//GEN-LAST:event_lVariablesValueChanged
    
    Variable getSelectedVariable () {
        int i = lVariables.getSelectedIndex ();
        if (i < 0) return null;
        return (Variable) variables.get (i);
    }
    
