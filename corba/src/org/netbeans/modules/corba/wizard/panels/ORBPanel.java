  private void bindingChanged (java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bindingChanged
// Add your handling code here:
      if (this.firstChange && !this.filter) {
         if (!warnUser()){
            rollBack();
            return;
         }
         this.firstChange = false;
      }
      String serverBind = (String)this.bindings.getSelectedItem();
      String clientBind = "";
      
      this.css.getActiveSetting ().setServerBinding (serverBind);
      if (serverBind == CORBASupport.SERVER_NS)
        clientBind = CORBASupport.CLIENT_NS;
      else if (serverBind == CORBASupport.SERVER_IOR_TO_FILE)
        clientBind = CORBASupport.CLIENT_IOR_FROM_FILE;
      else if (serverBind == CORBASupport.SERVER_IOR_TO_OUTPUT)
        clientBind = CORBASupport.CLIENT_IOR_FROM_INPUT;
      if (serverBind == CORBASupport.SERVER_BINDER)
        clientBind = CORBASupport.CLIENT_BINDER;
      this.css.getActiveSetting ().setClientBinding (clientBind);
  }//GEN-LAST:event_bindingChanged

  private void orbChanged (java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orbChanged
// Add your handling code here:
    if (this.firstChange && !this.filter) {
       if (!warnUser()) {
          rollBack();
          return;
       }
       this.firstChange = false;
    }
    this.css.setOrb ((String) this.orbs.getSelectedItem ());
    ORBSettingsWrapper[] list 
	= new ORBSettingsWrapper[this.css.getActiveSetting ().getServerBindings ().size ()];
    list = (ORBSettingsWrapper[])this.css.getActiveSetting ().getServerBindings ().toArray (list);
    this.bindings.removeAllItems();
    for ( int i=0; i< list.length; i++) {
      this.bindings.addItem (list[i]);
    }
  }//GEN-LAST:event_orbChanged

  private boolean warnUser () {
    TopManager tm = TopManager.getDefault ();
    NotifyDescriptor descriptor = new NotifyDescriptor.Confirmation (java.util.ResourceBundle.getBundle("org/netbeans/modules/corba/wizard/panels/Bundle").getString("MSG_WarnChange"), NotifyDescriptor.OK_CANCEL_OPTION);
    tm.notify (descriptor);
    if (descriptor.getValue() == NotifyDescriptor.CANCEL_OPTION)
       return false;
    else
       return true;
  }

  private void rollBack () {
    new Thread ( new Runnable () {
      public void run () {
        ORBPanel.this.filter = true;
        try {
          java.awt.EventQueue.invokeAndWait ( new Runnable () {
              public void run () {
                ORBPanel.this.orbs.setSelectedItem (defaultOrb);
                ORBPanel.this.bindings.setSelectedItem (defaultBinding);
              }});
        }catch (InterruptedException ie){}
        catch (java.lang.reflect.InvocationTargetException ite){}
        finally {
          ORBPanel.this.filter = false;
        }
      }}).start();
  }

