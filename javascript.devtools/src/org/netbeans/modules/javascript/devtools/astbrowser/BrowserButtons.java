    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         Node[] ns = TopComponent.getRegistry().getActivatedNodes();

        if (ns.length != 1) {
            return;
        }
        DataObject dataObject = (DataObject)ns[0].getLookup().lookup(DataObject.class);
        if (dataObject != null){
            jTextField1.setText(dataObject.getFolder().getPrimaryFile().getPath());
        }
        
//        fc.setSelectedFile(FileUtil.toFile(dataObject.getFolder().getPrimaryFile()));
        fc.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int returnVal = fc.showOpenDialog(this);
        
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            //This is where a real application would open the file.
            jTextField1.setText(fc.getSelectedFile().getAbsolutePath());
        }
   
    }//GEN-LAST:event_jButton3ActionPerformed

