    /**
     * Gets default instance. Do not use directly: reserved for *.settings files only,
     * i.e. deserialization routines; otherwise you could get a non-deserialized instance.
     * To obtain the singleton instance, use {@link findInstance}.
     */
    public static synchronized PkgBrowserTopComponent getDefault() {
        if (instance == null) {
            instance = new PkgBrowserTopComponent();
        }
        return instance;
    }
    
    /**
     * Obtain the PkgBrowserTopComponent instance. Never call {@link #getDefault} directly!
     */
    public static synchronized PkgBrowserTopComponent findInstance() {
        TopComponent win = WindowManager.getDefault().findTopComponent(PREFERRED_ID);
        if (win == null) {
            ErrorManager.getDefault().log(ErrorManager.WARNING, "Cannot find PkgBrowser component. It will not be located properly in the window system.");
            return getDefault();
        }
        if (win instanceof PkgBrowserTopComponent) {
            return (PkgBrowserTopComponent)win;
        }
        ErrorManager.getDefault().log(ErrorManager.WARNING, "There seem to be multiple components with the '" + PREFERRED_ID + "' ID. That is a potential source of errors and unexpected behavior.");
        return getDefault();
    }
    
    public int getPersistenceType() {
        return TopComponent.PERSISTENCE_ALWAYS;
    }
    
    public void componentOpened() {
        mgr.setRootContext(new AbstractNode (new PackagesRootChildren()));
    }
    
    public void componentClosed() {
        mgr.setRootContext (new AbstractNode (Children.LEAF));
    }
    
    /** replaces this in object stream */
    public Object writeReplace() {
        return new ResolvableHelper();
    }
    
    protected String preferredID() {
        return PREFERRED_ID;
    }

    private final ExplorerManager mgr = new ExplorerManager();
    public ExplorerManager getExplorerManager() {
        return mgr;
    }
    
    final static class ResolvableHelper implements Serializable {
        private static final long serialVersionUID = 1L;
        public Object readResolve() {
            return PkgBrowserTopComponent.getDefault();
        }
    }
    
