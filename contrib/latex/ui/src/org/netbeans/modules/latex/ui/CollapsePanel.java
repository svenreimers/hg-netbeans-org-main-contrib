    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Add your handling code here:
        int[] sel = jList1.getSelectedIndices();
        Collection toRemove = new ArrayList();
        
        for (int cntr = 0; cntr < sel.length; cntr++) {
            toRemove.add(listModel.get(sel[cntr]));
        }
        
        Iterator remove = toRemove.iterator();
        
        while (remove.hasNext()) {
            listModel.removeElement(remove.next());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Add your handling code here:
        String toAdd = jTextField1.getText();
        
        if (!listModel.contains(toAdd))
            listModel.addElement(toAdd);
    }//GEN-LAST:event_jButton1ActionPerformed
    
