package beans2nbm.gen;

import java.io.IOException;
import java.io.OutputStream;
import java.util.Properties;

/**
 *
 * @author Tim Boudreau
 */
public class PropertiesFileModel extends Properties implements FileModel {
    private String path;
    public PropertiesFileModel(String path) {
        this.path = path;
    }

    public String getPath() {
        return path;
    }

    public void write(OutputStream stream) throws IOException {
        super.store(stream, "Generated by Beans2NBM");
    }
    
}
