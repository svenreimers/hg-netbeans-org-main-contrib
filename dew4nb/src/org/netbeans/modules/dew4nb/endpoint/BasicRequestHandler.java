package org.netbeans.modules.dew4nb.endpoint;

import org.netbeans.modules.dew4nb.endpoint.RequestHandler;
import java.lang.reflect.Method;
import java.util.logging.Level;
import java.util.logging.Logger;
import org.netbeans.api.annotations.common.NonNull;
import org.openide.util.Parameters;
import org.openide.util.lookup.ServiceProvider;

/** A request handler to be registered by {@link ServiceProvider}
 * annotation and convert requests for some functionality of 
 * Javac to some responses.
 *
 * @author Jaroslav Tulach <jtulach@netbeans.org>
 * @author Tomas Zezula
 */
public abstract class BasicRequestHandler<Request,RequestKind extends Enum<RequestKind>, Response> extends RequestHandler<Request, RequestKind> {
    
    private static final Logger LOG = Logger.getLogger(BasicRequestHandler.class.getName());

    final Class<Response> responseType;

    protected BasicRequestHandler(
        @NonNull final String endPointName,
        @NonNull final RequestKind requestKind,
        @NonNull final Class<Request> requestType,
        @NonNull final Class<Response> responseType) {
        super(endPointName, requestKind, requestType);
        Parameters.notNull("responseType", responseType);   //NOI18N
        this.responseType = responseType;
    }
    
    /** Handle an incomming request, if one is able to.
     * 
     * @param request
     * @param response
     * @return Status
     */
    protected abstract Status handle(Request request, Response response);

    @Override
    @NonNull
    Status perform (
            @NonNull final Request query,
            @NonNull final EndPoint.Env env) throws Exception {
        Parameters.notNull("query", query); //NOI18N
        final Response res = responseType.getDeclaredConstructor().newInstance();
        final Status status = handle(query, res);
        try {
            final Method get = requestType.getDeclaredMethod("getType");    //NOI18N
            final Object val = get.invoke(query);
            final Method set = responseType.getDeclaredMethod("setType", requestKind.getDeclaringClass()); //NOI18N
            set.invoke(res, val);
        } catch (NoSuchMethodException noProp) {
            LOG.log(
                Level.WARNING,
                "The {0} or {1} has no type property.",  //NOI18N
                new Object[] {
                    query,
                    res
                });
        }
        try {
            final Method get = requestType.getDeclaredMethod("getState");    //NOI18N
            final Object val = get.invoke(query);
            final Method set = responseType.getDeclaredMethod("setState", String.class);  //NOI18N
            set.invoke(res, val);
        } catch (NoSuchMethodException noProp) {
            LOG.log(
                Level.WARNING,
                "The {0} or {1} has no state property.",  //NOI18N
                new Object[] {
                    query,
                    res
                });
        }
        try {
            final Method set = responseType.getDeclaredMethod("setStatus", Status.class);  //NOI18N
            set.invoke(res, status);
        } catch (NoSuchMethodException noProp) {
            LOG.log(
                Level.WARNING,
                "The {0} has no status property.",  //NOI18N
                res);
        }
        env.sendObject(res);
        return Status.done;
    }
}
